    private void agregarTuristaTourActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarTuristaTourActionPerformed
        int n2 = verTuristas.getSelectedItem().toString().length();
        int n3 = verTours.getSelectedItem().toString().length();
        
        //seleccionar RUT desde el combobox (RUT al final)
        String RUT = verTuristas.getSelectedItem().toString().substring(n2-11, n2);
        if(Character.isDigit(verTuristas.getSelectedItem().toString().charAt(n2-12))){
            RUT = verTuristas.getSelectedItem().toString().substring(n2-12, n2);
        }

        String IDt = verTours.getSelectedItem().toString().substring(n3-1, n3);
        if(Character.isDigit(verTours.getSelectedItem().toString().charAt(n3-2))){
            IDt = verTours.getSelectedItem().toString().substring(n3-2, n3);
        }
        
        rs = consulta.hacerConsulta("SELECT edad_turista FROM Turistas WHERE RUT_turista = '"+RUT+"'");
        String preedad = null;
        try {
            while(rs.next()){
                preedad = rs.getString("edad_turista");
                break;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error 1 al obtener edad del Turista." + ex.getMessage());
        }
        double edad = 0, descuento = 0;
        try {
            edad = Integer.parseInt(preedad);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error 2 al obtener edad del Turista." + ex.getMessage());
        }
        if(edad>=0 && edad<=15){
            descuento = 0.2;
        }
        else if(edad>=51){
            descuento = 0.1;
        }
        else if(edad>=66){
            descuento = 0.3;
        }

        updateSQL = "INSERT INTO Jornada(c√≥digo_tour, RUT_turista, descuento) "
        + "VALUES "
        + "("
        + IDt + ", '"
        + RUT + "', "
        + descuento + ") ";

        try{
            modificacion.hacerModificacion(updateSQL);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al agregar." + e.getMessage());
        }
        this.setVisible(false);
    }//GEN-LAST:event_agregarTuristaTourActionPerformed

