    private void asociarLugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_asociarLugarActionPerformed
        try {
            VentanaAsociarLugarTour ventanaAsociarLugarTour = new VentanaAsociarLugarTour();
            ventanaAsociarLugarTour.setModificacion(modificacion);
            ventanaAsociarLugarTour.setConsulta(consulta);
            
            String consultaLugarSQL = "SELECT nombre_lugar, código_lugar "
                    + "FROM Lugar "
                    + "ORDER BY código_lugar";
            
            //Consulta para llenar combobox con Nombre y Código de los Lugares.
            rs = consulta.hacerConsulta(consultaLugarSQL);
            while(rs.next()){
                ventanaAsociarLugarTour.getVerLugares().addItem(rs.getString("nombre_lugar") + " - " + rs.getString("código_lugar"));
            }
            
            String consultaTourSQL = "SELECT código_tour, nombre_repres "
                    + "FROM Tour "
                    + "ORDER BY código_tour";
            
            //Consulta para llenar combobox con Nombre y Código de los Tours.
            rs = consulta.hacerConsulta(consultaTourSQL);
            while(rs.next()){
                ventanaAsociarLugarTour.getVerTours().addItem(rs.getString("nombre_repres") + ", con ID Tour: " + rs.getString("código_tour"));
            }
            
            ventanaAsociarLugarTour.setVisible(true);
        } catch (SQLException ex) {
            Logger.getLogger(Intranet.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_asociarLugarActionPerformed

