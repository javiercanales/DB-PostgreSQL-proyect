    private void agregarTourActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarTourActionPerformed
        int n = verCiudades.getSelectedItem().toString().length();
        String IDc = verCiudades.getSelectedItem().toString().substring(n-1, n);
        
        if(Character.isDigit(verCiudades.getSelectedItem().toString().charAt(n-2))){
            IDc = verCiudades.getSelectedItem().toString().substring(n-2, n);
        }
        
        updateSQL = "INSERT INTO Tour(nombre_repres, mínimo_grupal, costo_indiv, costo_grupal, cod_ciudad) "
                  + "VALUES "
                  + "('"
                  + nombreTour.getText()+"', "
                  + minimoGrupal.getText() + ", "
                  + costoIndiv.getText()+", "
                  + costoGrupal.getText()+", "
                  + IDc +")";
        
        try{
            modificacion.hacerModificacion(updateSQL);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al agregar.");
        }
    }//GEN-LAST:event_agregarTourActionPerformed

